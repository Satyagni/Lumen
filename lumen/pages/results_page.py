        # Convert to QImage and register as resource
        byte_arr = io.BytesIO()
        overlay_resized.save(byte_arr, format='PNG')
        qimage = QImage.fromData(byte_arr.getvalue())
        doc.addResource(QTextDocument.ImageResource, QUrl("preview_overlay.png"), qimage)

